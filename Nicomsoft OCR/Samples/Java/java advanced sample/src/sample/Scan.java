    public void Init(HSCAN ScanObj, HIMG ImgObj)
    {
        this.ScanObj = ScanObj;
        this.ImgObj  = ImgObj;
        
        cbSource.setSelectedIndex(0);
	cbDevice.removeAllItems();

	int res;
        
        StringBuffer ScannerName = new StringBuffer();

	res =  NSOCR.Engine.Scan_Enumerate(ScanObj, ScannerName, 0);
        
	if (res > NSOCR.Error.ERROR_FIRST)
	{
                EnumOK = false;
		JOptionPane.showMessageDialog(this, "Scan_Enumerate" + res);
		return;
	}

//names are separated by comma, parse them
        StringTokenizer tokenizer = new StringTokenizer(ScannerName.toString(), ",");                
	while (tokenizer.hasMoreElements())
	{
            cbDevice.addItem(tokenizer.nextToken());
	}

//now get default device index
	res = NSOCR.Engine.Scan_Enumerate(ScanObj, null, NSOCR.Constant.SCAN_GETDEFAULTDEVICE);
	if (res > NSOCR.Error.ERROR_FIRST)
	{
		if (res != NSOCR.Error.ERROR_NODEFAULTDEVICE)
		{
                        EnumOK = false;
                        JOptionPane.showMessageDialog(this, "Scan_Enumerate (1)" + res);
			return;
		}
	}
	else
	{
		if (res < cbDevice.getItemCount()) cbDevice.getSelectedIndex();
	}        
    }


