    private void btScanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btScanActionPerformed
	int n, flags, res;
	n = cbDevice.getSelectedIndex();
        
	if (n < 0)
	{
		JOptionPane.showMessageDialog(this, "No scanner selected!");
		return;
	}

	flags = 0;
	switch (cbSource.getSelectedIndex())
	{
            case 0: break; //flatbed
            case 1: flags = flags | NSOCR.Constant.SCAN_SOURCEADF; break; //adf
            case 2: flags = flags | NSOCR.Constant.SCAN_SOURCEAUTO; break; //auto
	}

	if (cbNoUI.isSelected())
            flags = flags | NSOCR.Constant.SCAN_NOUI;

	res = NSOCR.Engine.Scan_ScanToImg(ScanObj, ImgObj, n, flags);
	
	if (res > NSOCR.Error.ERROR_FIRST)
	{
		JOptionPane.showMessageDialog(this, "Scan_ScanToImg" + res);
		return;
	}
        
        this.dispose();
        isOk = true;
    }//GEN-LAST:event_btScanActionPerformed

    
